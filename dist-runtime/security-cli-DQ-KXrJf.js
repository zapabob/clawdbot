async function safeChmod(params) {
  try {
    const st = await fs.lstat(params.path);
    if (st.isSymbolicLink())
      return {
        kind: "chmod",
        path: params.path,
        mode: params.mode,
        ok: false,
        skipped: "symlink",
      };
    if (params.require === "dir" && !st.isDirectory())
      return {
        kind: "chmod",
        path: params.path,
        mode: params.mode,
        ok: false,
        skipped: "not-a-directory",
      };
    if (params.require === "file" && !st.isFile())
      return {
        kind: "chmod",
        path: params.path,
        mode: params.mode,
        ok: false,
        skipped: "not-a-file",
      };
    if ((st.mode & 511) === params.mode)
      return {
        kind: "chmod",
        path: params.path,
        mode: params.mode,
        ok: false,
        skipped: "already",
      };
    await fs.chmod(params.path, params.mode);
    return {
      kind: "chmod",
      path: params.path,
      mode: params.mode,
      ok: true,
    };
  } catch (err) {
    if (err.code === "ENOENT")
      return {
        kind: "chmod",
        path: params.path,
        mode: params.mode,
        ok: false,
        skipped: "missing",
      };
    return {
      kind: "chmod",
      path: params.path,
      mode: params.mode,
      ok: false,
      error: String(err),
    };
  }
}
