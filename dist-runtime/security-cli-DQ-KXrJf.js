async function fixSecurityFootguns(opts) {
  const env = opts?.env ?? process.env;
  const platform = opts?.platform ?? process.platform;
  const exec = opts?.exec ?? runExec;
  const isWindows = platform === "win32";
  const stateDir = opts?.stateDir ?? resolveStateDir(env);
  const configPath = opts?.configPath ?? resolveConfigPath(env, stateDir);
  const actions = [];
  const errors = [];
  const io = createConfigIO({
    env,
    configPath,
  });
  const snap = await io.readConfigFileSnapshot();
  if (!snap.valid) errors.push(...snap.issues.map((i) => `${i.path}: ${i.message}`));
  let configWritten = false;
  let changes = [];
  if (snap.valid) {
    const fixed = applyConfigFixes({
      cfg: snap.config,
      env,
    });
    changes = fixed.changes;
    const whatsappStoreAllowFrom = await readChannelAllowFromStore(
      "whatsapp",
      env,
      DEFAULT_ACCOUNT_ID,
    ).catch(() => []);
    if (whatsappStoreAllowFrom.length > 0)
      setWhatsAppGroupAllowFromFromStore({
        cfg: fixed.cfg,
        storeAllowFrom: whatsappStoreAllowFrom,
        changes,
        policyFlips: fixed.policyFlips,
      });
    if (changes.length > 0)
      try {
        await io.writeConfigFile(fixed.cfg);
        configWritten = true;
      } catch (err) {
        errors.push(`writeConfigFile failed: ${String(err)}`);
      }
  }
  const applyPerms = (params) =>
    isWindows
      ? safeAclReset({
          path: params.path,
          require: params.require,
          env,
          exec,
        })
      : safeChmod({
          path: params.path,
          mode: params.mode,
          require: params.require,
        });
  actions.push(
    await applyPerms({
      path: stateDir,
      mode: 448,
      require: "dir",
    }),
  );
  actions.push(
    await applyPerms({
      path: configPath,
      mode: 384,
      require: "file",
    }),
  );
  if (snap.exists) {
    const includePaths = await collectIncludePathsRecursive({
      configPath: snap.path,
      parsed: snap.parsed,
    }).catch(() => []);
    for (const p of includePaths)
      actions.push(
        await applyPerms({
          path: p,
          mode: 384,
          require: "file",
        }),
      );
  }
  await chmodCredentialsAndAgentState({
    env,
    stateDir,
    cfg: snap.config ?? {},
    actions,
    applyPerms,
  }).catch((err) => {
    errors.push(`chmodCredentialsAndAgentState failed: ${String(err)}`);
  });
  return {
    ok: errors.length === 0,
    stateDir,
    configPath,
    configWritten,
    changes,
    actions,
    errors,
  };
}
